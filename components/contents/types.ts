/**
 * Theme-aware foreground/background colors for a status badge or pill. Pulls from
 * the central palette so components carry no hardcoded hex values.
 */
export function contentStatusColors(
    status: ContentStatus,
    colors: ReturnType<typeof Colors>
): { fg: string; bg: string } {
    switch (status) {
        case "in_progress":
            return { fg: colors.statusInProgressFg, bg: colors.statusInProgressBg };
        case "review_pending":
            return { fg: colors.statusReviewFg, bg: colors.statusReviewBg };
        case "approved":
            return { fg: colors.statusApprovedFg, bg: colors.statusApprovedBg };
        case "scheduled":
            return { fg: colors.statusScheduledFg, bg: colors.statusScheduledBg };
        case "posted":
            return { fg: colors.statusPostedFg, bg: colors.statusPostedBg };
        case "rejected":
            return { fg: colors.statusRejectedFg, bg: colors.statusRejectedBg };
        case "draft":
        default:
            return { fg: colors.statusDraftFg, bg: colors.statusDraftBg };
    }
}

