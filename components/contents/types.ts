import { CalendarItem } from "@/components/content-calendar/types";
import { Attachment } from "@/shared-libs/firestore/trendly-pro/constants/attachment";
import { IImageGeneration } from "@/shared-libs/firestore/trendly-pro/models/contents";
import Colors from "@/shared-uis/constants/Colors";

/**
 * UI content status — adopts the full 6-value Firestore enum
 * (Draft / PendingReview / Approved / Scheduled / Posted / Rejected).
 * The string values are identical to the Firestore `ContentStatus` enum, so the
 * two are interchangeable without a mapping table.
 */
export type ContentStatus =
    | "draft"
    | "in_progress"
    | "review_pending"
    | "approved"
    | "scheduled"
    | "posted"
    | "rejected";

/** A single uploaded or AI-generated media asset attached to a content piece. */
export interface MediaAsset {
    /** Public URL of the asset (S3, via useAWSContext). */
    url: string;
    kind: "image" | "video";
    /** Pixel dimensions — used for per-type aspect-ratio enforcement (Phase 2). */
    width?: number;
    height?: number;
    /** Order within a carousel (0-based); undefined for single-asset types. */
    order?: number;
    /** Whether the asset was uploaded by the user or generated by AI. */
    source: "uploaded" | "generated";
}

/** A connected social account this content will be published / scheduled to. */
export interface SocialDestination {
    /** Matches `ISocialAccount.id` from `useBrandSocialContext`. */
    socialAccountId: string;
    platform: "instagram" | "facebook" | "youtube" | "linkedin" | "twitter";
    /** Cached for display so the picker/preview don't need to re-query. */
    username?: string;
}

export type ScheduleMode = "now" | "scheduled";

export interface ContentItem extends CalendarItem {
    status: ContentStatus;
    caption?: string;
    hashtags?: string;
    timeOfPosting?: string; // "HH:MM"
    script?: string;
    imagePrompt?: string;
    /** Final media files / carousel slides. Mirrors `IContent.attachments`. */
    attachments?: Attachment[];
    /** Live state of a backend-driven AI image-generation job, if any. */
    imageGeneration?: IImageGeneration;
    /** Target connected accounts for publish / schedule (Phase 4). */
    destinations?: SocialDestination[];
    /** Publish immediately or at `scheduledAt`. */
    scheduleMode?: ScheduleMode;
    /** Epoch ms when a scheduled post should go live. */
    scheduledAt?: number;
    /**
     * Per-platform published media ids, set by the backend once posted
     * (e.g. `{ instagram: "<mediaId>" }`). Mirrors `IContent.publishedIds`.
     * Used to look up the live post's analytics + comments.
     */
    publishedIds?: Record<string, string>;
    /** Permalink to the live post once posted. Mirrors `IContent.postedUrl`. */
    postedUrl?: string;
    isArchived: boolean;
    createdAt: string;
}

